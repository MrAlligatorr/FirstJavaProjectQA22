package remakeHomework.lab3IfElse;

public class CalculatorRemake {
    public  int sum(int x, int y) {
        return x + y;
    }

    public  float divide(int x, float y) {
        return x / y;
    }

    public  int minus(int x, int y) {
        return x - y;
    }

    public  int multiply(int x, int y) {
        return x * y;
    }

    public  float average(int x, int y, int z) {
        return ((x + y + z) / 3f);
    }

    public  int decimal(int x, int y) {
        return x % y;
    }
}
